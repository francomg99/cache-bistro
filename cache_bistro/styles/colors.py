from enum import Enum


class Color(Enum):
    ACCENT = "#060606"
    PRIMARY = "#bb9a56"
    SECONDARY = "#fefef5"
    TERTIARY = "#fec6c7"
    CUARTIARY = "#092f31"


class TextColor(Enum):
    ACCENT = "#060606 !important"
    PRIMARY = "#fefef5"
    SECONDARY = "#bb9a56"
    TERTIARY = "#fec6c7"
    CUARTIARY = "#092f31"
